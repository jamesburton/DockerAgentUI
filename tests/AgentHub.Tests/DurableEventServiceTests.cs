namespace AgentHub.Tests;

public class DurableEventServiceTests
{
    private static AgentHubDbContext CreateInMemoryDb(string? dbName = null)
    {
        var options = new DbContextOptionsBuilder<AgentHubDbContext>()
            .UseInMemoryDatabase(dbName ?? Guid.NewGuid().ToString())
            .Options;
        return new AgentHubDbContext(options);
    }

    private static IDbContextFactory<AgentHubDbContext> CreateFactory(string? dbName = null)
    {
        var name = dbName ?? Guid.NewGuid().ToString();
        var options = new DbContextOptionsBuilder<AgentHubDbContext>()
            .UseInMemoryDatabase(name)
            .Options;
        return new InMemoryDbContextFactory(options);
    }

    /// <summary>
    /// Simple IDbContextFactory for InMemory provider that shares the same DB name.
    /// </summary>
    private sealed class InMemoryDbContextFactory : IDbContextFactory<AgentHubDbContext>
    {
        private readonly DbContextOptions<AgentHubDbContext> _options;
        public InMemoryDbContextFactory(DbContextOptions<AgentHubDbContext> options) => _options = options;
        public AgentHubDbContext CreateDbContext() => new AgentHubDbContext(_options);
    }

    private static SessionEvent MakeEvent(string sessionId = "session-1", string data = "test data")
    {
        return new SessionEvent(sessionId, SessionEventKind.Info, DateTimeOffset.UtcNow, data);
    }

    private static async Task SeedSession(IDbContextFactory<AgentHubDbContext> factory, string sessionId = "session-1")
    {
        await using var db = factory.CreateDbContext();
        if (!await db.Sessions.AnyAsync(s => s.SessionId == sessionId))
        {
            db.Sessions.Add(new SessionEntity
            {
                SessionId = sessionId,
                OwnerUserId = "dev-user",
                State = SessionState.Running,
                CreatedUtc = DateTimeOffset.UtcNow,
                Backend = "in-memory",
                RequirementsJson = "{}"
            });
            await db.SaveChangesAsync();
        }
    }

    [Fact]
    public async Task EmitAsync_PersistsEventToDb_WithAutoGeneratedId()
    {
        // Arrange
        var dbName = Guid.NewGuid().ToString();
        var factory = CreateFactory(dbName);
        await SeedSession(factory);
        var subs = new SseSubscriptionManager();
        var service = new DurableEventService(factory, subs);
        var ev = MakeEvent(data: "persisted event");

        // Act
        await service.EmitAsync(ev);

        // Assert
        await using var db = factory.CreateDbContext();
        var stored = await db.Events.FirstOrDefaultAsync();
        Assert.NotNull(stored);
        Assert.True(stored.Id > 0, "Event should have auto-generated Id > 0");
        Assert.Equal("persisted event", stored.Data);
        Assert.Equal("session-1", stored.SessionId);
        Assert.Equal(SessionEventKind.Info, stored.Kind);
    }

    [Fact]
    public async Task EmitAsync_BroadcastsToSessionSubscribers()
    {
        // Arrange
        var factory = CreateFactory();
        await SeedSession(factory);
        var subs = new SseSubscriptionManager();
        var service = new DurableEventService(factory, subs);

        var ch = Channel.CreateUnbounded<SseItem<SessionEvent>>();
        var connId = Guid.NewGuid();
        subs.AddSessionSubscriber("session-1", connId, ch.Writer);

        // Act
        await service.EmitAsync(MakeEvent(data: "broadcast test"));

        // Assert
        Assert.True(ch.Reader.TryRead(out var received));
        Assert.Equal("broadcast test", received.Data.Data);
        Assert.NotNull(received.EventId);
    }

